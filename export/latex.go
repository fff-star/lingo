// ArticlesExportData holds data for the multi-article LaTeX template.
type ArticlesExportData struct {
	Articles  []ArticleData
	TagFilter string
}

// GenerateArticlesTeX renders multiple articles as a single LaTeX document.
func GenerateArticlesTeX(articles []model.Article, tagFilter string) (string, error) {
	var items []ArticleData
	for _, a := range articles {
		item := ArticleData{
			Title:   escapeLaTeX(a.Title),
			Author:  escapeLaTeX(a.Author),
			Date:    formatDate(a.CreatedAt),
			Content: a.Content,
			Summary: a.Summary,
			Tags:    escapeLaTeX(strings.Join(a.Tags, ", ")),
		}
		if a.AIAnalysis != nil {
			item.AIAnalysis = cleanAnalysis(a.AIAnalysis)
		}
		items = append(items, item)
	}

	var buf bytes.Buffer
	err := multiArticleTemplate.Execute(&buf, ArticlesExportData{Articles: items, TagFilter: tagFilter})
	if err != nil {
		return "", fmt.Errorf("render articles tex: %w", err)
	}
	return buf.String(), nil
}

