// ArticleData holds data for the article LaTeX template.
type ArticleData struct {
	Title      string
	Author     string
	Date       string
	Source     string
	SourceURL  string
	Content    string
	Summary    string
	Tags       string
	AIAnalysis *model.AIAnalysis
}

// GenerateArticleTeX renders a single article as a LaTeX document.
func GenerateArticleTeX(a model.Article) (string, error) {
	data := ArticleData{
		Title:     escapeLaTeX(a.Title),
		Author:    escapeLaTeX(a.Author),
		Date:      formatDate(a.CreatedAt),
		Source:    escapeLaTeX(a.Source),
		SourceURL: a.SourceURL,
		Content:   a.Content,
		Summary:   a.Summary,
		Tags:      escapeLaTeX(strings.Join(a.Tags, ", ")),
	}

	if a.AIAnalysis != nil {
		data.AIAnalysis = cleanAnalysis(a.AIAnalysis)
	}

	var buf bytes.Buffer
	err := articleTemplate.Execute(&buf, data)
	if err != nil {
		return "", fmt.Errorf("render article tex: %w", err)
	}
	return buf.String(), nil
}

