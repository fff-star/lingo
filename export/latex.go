func escapeLaTeX(s string) string {
	replacer := strings.NewReplacer(
		`\`, `\textbackslash{}`,
		`{`, `\{`,
		`}`, `\}`,
		`$`, `\$`,
		`&`, `\&`,
		`#`, `\#`,
		`^`, `\^{}`,
		`_`, `\_`,
		`~`, `\textasciitilde{}`,
		`%`, `\%`,
	)
	return replacer.Replace(s)
}

func formatDate(t time.Time) string {
	if t.IsZero() {
		return ""
	}
	return t.Format("2006-01-02")
}

func wordRow(w model.Word) string {
	defs := []string{}
	for _, d := range w.ECDictDefs {
		defs = append(defs, fmt.Sprintf(`[%s] %s`, escapeLaTeX(d.Pos), escapeLaTeX(d.Meaning)))
	}
	for _, d := range w.Definitions {
		defs = append(defs, fmt.Sprintf(`[%s] %s`, escapeLaTeX(d.Pos), escapeLaTeX(d.Meaning)))
	}
	def := strings.Join(defs, `; `)
	if def == "" {
		def = "---"
	}
	phonetic := w.Phonetic
	if phonetic == "" {
		phonetic = "---"
	}
	tags := escapeLaTeX(strings.Join(w.Tags, ", "))
	return fmt.Sprintf(`%s & %s & %s & %s \\`,
		escapeLaTeX(w.Word), phonetic, def, tags)
}

// WordListData holds all data for the word list template.
type WordListData struct {
	Words     []model.Word
	TagFilter string
}

// GenerateWordsTeX renders a word list as a LaTeX longtable document.
func GenerateWordsTeX(words []model.Word, tagFilter string) (string, error) {
	tmpl := template.Must(template.New("words").Funcs(template.FuncMap{
		"escape":  escapeLaTeX,
		"join":    strings.Join,
		"wordRow": wordRow,
	}).Parse(`\documentclass[12pt,a4paper]{ctexart}
\usepackage{longtable,booktabs,geometry}
\geometry{margin=2cm}
\usepackage[colorlinks=true,linkcolor=black]{hyperref}

\title{Word List{{if .TagFilter}}: {{.TagFilter}}{{end}} }
\date{\today}
\author{Generated by Lingo}

\begin{document}
\maketitle

\setlength{\tabcolsep}{6pt}
\begin{longtable}{p{4.5cm}p{3.5cm}p{6cm}p{2.5cm}}
\toprule
\textbf{Word} & \textbf{Phonetic} & \textbf{Definition} & \textbf{Tags} \\
\midrule
\endhead
\midrule
\multicolumn{4}{r}{\footnotesize Continued on next page}\\
\endfoot
\bottomrule
\endlastfoot
{{range .Words}}
{{wordRow .}}{{end}}
\end{longtable}
\end{document}
`))

	var buf bytes.Buffer
	err := tmpl.Execute(&buf, WordListData{Words: words, TagFilter: tagFilter})
	if err != nil {
		return "", fmt.Errorf("render words tex: %w", err)
	}
	return buf.String(), nil
}

