// CompositionData holds data for the composition LaTeX template.
type CompositionData struct {
	Title      string
	Author     string
	Date       string
	Topic      string
	Content    string
	Tags       string
	Notes      string
	AIAnalysis *model.AIAnalysis
}

// GenerateCompositionTeX renders a single composition as a LaTeX document.
func GenerateCompositionTeX(c model.Composition) (string, error) {
	data := CompositionData{
		Title:  escapeLaTeX(c.Title),
		Author: escapeLaTeX(c.Author),
		Date:   formatDate(c.CreatedAt),
		Topic:  escapeLaTeX(c.Topic),
		Tags:   escapeLaTeX(strings.Join(c.Tags, ", ")),
		Notes:  c.Notes,
	}

	// Content is markdown; preserve it as-is (LaTeX will render it as plain text).
	data.Content = c.Content

	if c.AIAnalysis != nil {
		data.AIAnalysis = cleanAnalysis(c.AIAnalysis)
	}

	var buf bytes.Buffer
	err := compositionTemplate.Execute(&buf, data)
	if err != nil {
		return "", fmt.Errorf("render composition tex: %w", err)
	}
	return buf.String(), nil
}

// cleanAnalysis returns a copy of the AI analysis with escaped text fields.
func cleanAnalysis(a *model.AIAnalysis) *model.AIAnalysis {
	if a == nil {
		return nil
	}
	out := *a

	out.Summary = a.Summary // pass through, LaTeX-compatible
	for i := range out.Words {
		out.Words[i].Word = escapeLaTeX(out.Words[i].Word)
		out.Words[i].Example = escapeLaTeX(out.Words[i].Example)
		out.Words[i].Notes = escapeLaTeX(out.Words[i].Notes)
		for j := range out.Words[i].Definitions {
			out.Words[i].Definitions[j].Pos = escapeLaTeX(out.Words[i].Definitions[j].Pos)
			out.Words[i].Definitions[j].Meaning = escapeLaTeX(out.Words[i].Definitions[j].Meaning)
		}
	}
	for i := range out.Phrases {
		out.Phrases[i].Phrase = escapeLaTeX(out.Phrases[i].Phrase)
		out.Phrases[i].Type = escapeLaTeX(out.Phrases[i].Type)
		out.Phrases[i].Definition = escapeLaTeX(out.Phrases[i].Definition)
		out.Phrases[i].Example = escapeLaTeX(out.Phrases[i].Example)
	}
	for i := range out.Sentences {
		out.Sentences[i].Text = escapeLaTeX(out.Sentences[i].Text)
		out.Sentences[i].Translation = escapeLaTeX(out.Sentences[i].Translation)
		out.Sentences[i].Why = escapeLaTeX(out.Sentences[i].Why)
	}
	for i := range out.GrammarErrors {
		out.GrammarErrors[i].Sentence = escapeLaTeX(out.GrammarErrors[i].Sentence)
		out.GrammarErrors[i].Correction = escapeLaTeX(out.GrammarErrors[i].Correction)
		out.GrammarErrors[i].Explanation = escapeLaTeX(out.GrammarErrors[i].Explanation)
		out.GrammarErrors[i].ErrorType = escapeLaTeX(out.GrammarErrors[i].ErrorType)
	}
	if out.ModelEssay2 != nil {
		for i := range out.ModelEssay2.Words {
			out.ModelEssay2.Words[i].Word = escapeLaTeX(out.ModelEssay2.Words[i].Word)
		}
	}

	return &out
}

